package tk.siastv.xh;

public class While1 {
    public static void main(String[] args) {
        //目标：学会while循环
        int i1 = 0;
        while (i1 < 3){
            System.out.println("HelloWord");
            i1++;
        }
        System.out.println("------------------");
        int i2 = 0;
        while (i2 < 3){
            System.out.println("HelloWord");
            i2++;
        }
    }
}
