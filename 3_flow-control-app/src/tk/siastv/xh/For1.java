package tk.siastv.xh;

public class For1 {
    public static void main(String[] args) {
        //目标：学会使用for循环，并理解它的执行国产。
        //需求：输出3次HelloWord
        for (int i = 0; i < 3; i++) {
            System.out.println("HelloWord");
        }

        System.out.println("--------------------");
        for (int i = 1; i < 5; i++) {
            System.out.println("HelloWord");
        }
    }
}
