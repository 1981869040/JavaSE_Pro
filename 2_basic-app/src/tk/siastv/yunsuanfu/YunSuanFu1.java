package tk.siastv.yunsuanfu;

public class YunSuanFu1 {
    public static void main(String[] args) {
        int a1 = 10;
        int b1 = 3;
        System.out.println(a1 + b1);
        System.out.println(a1 - b1);
        System.out.println(a1 * b1);
        System.out.println(a1 / 1.0 / b1);
        System.out.println(a1 / b1 * 1.0);
        System.out.println((double) a1 / b1);
        System.out.println(a1 % b1);

        double c1 = (double) a1 / b1;
        System.out.println(c1);
    }
}
